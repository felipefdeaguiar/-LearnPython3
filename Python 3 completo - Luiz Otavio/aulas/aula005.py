print('Soma 10 + 5 = ', 10 + 5)
print('Subtração de 10 - 5 = ', 10 - 5)
print('Multiplicação de 10 * 5 = ', 10 * 5)
print('Divisão de 10 / 5 = ', 10 / 5)
print('Divisão inteira de 10 / 4.9 = ', 10 // 4.9)
print('Potenciação de 10 elevado à quinta potência = ', 10 ** 5)
print('Resto da divisão de 10 / 5 = ', 10 % 5)

# ! Alteração de precedência matemática ()
print('Resultado de 5 + 2 * 10 = ', 5+2*10)
print('Resultado de (5 + 2) * 10 = ', (5+2)*10)

